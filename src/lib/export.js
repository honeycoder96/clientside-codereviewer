/**
 * Human-readable JSON export of the full review result.
 *
 * @param {object}              diffReview
 * @param {Map<string,object>}  fileReviews
 * @param {object[]}            files
 * @returns {string}
 */
export function toJSON(diffReview, fileReviews, files) {
  const fileMeta = new Map(files.map((f) => [f.filename, f]))

  const payload = {
    exportedAt:     new Date().toISOString(),
    overallRisk:    diffReview.overallRisk,
    totalIssues:    diffReview.totalIssues,
    durationMs:     diffReview.durationMs,
    totalTokens:    diffReview.totalTokens,
    commitMessage:  diffReview.commitMessage  ?? '',
    suggestedTests: diffReview.suggestedTests ?? [],
    files: sortedFileReviews(fileReviews).map((fr) => {
      const meta = fileMeta.get(fr.filename)
      return {
        filename:   fr.filename,
        status:     meta?.status     ?? 'modified',
        additions:  meta?.additions  ?? 0,
        deletions:  meta?.deletions  ?? 0,
        riskScore:  fr.riskScore,
        issueCount: fr.issueCount,
        issues: sortedIssues(fr.mergedIssues).map((i) => ({
          line:       i.line,
          severity:   i.severity,
          category:   i.category,
          message:    i.message,
          suggestion: i.suggestion ?? null,
          foundBy:    i.foundBy    ?? null,
        })),
      }
    }),
  }

  return JSON.stringify(payload, null, 2)
}
