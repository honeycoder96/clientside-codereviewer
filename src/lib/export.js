/**
 * Produces a structured, GitHub-renderable Markdown report.
 *
 * @param {object}              diffReview  — result object from reviewDiff()
 * @param {Map<string,object>}  fileReviews — Map<filename, FileReview> from store
 * @param {object[]}            files       — FileDiff[] from store
 * @returns {string}
 */
export function toMarkdown(diffReview, fileReviews, files) {
  const {
    overallRisk,
    totalIssues,
    durationMs,
    totalTokens,
    commitMessage,
    suggestedTests,
  } = diffReview

  const { label: riskLbl } = riskLabel(overallRisk)
  const seconds  = durationMs ? (durationMs / 1000).toFixed(1) : '—'
  const tokStr   = totalTokens > 0 ? totalTokens.toLocaleString() + ' tokens' : ''
  const fileMeta = new Map(files.map((f) => [f.filename, f]))
  const lines    = []

  // ── Header ──────────────────────────────────────────────────────────────
  lines.push('# Code Review Report')
  lines.push(`> Generated by WebGPU Code Reviewer · ${new Date().toISOString()}`)
  lines.push('')
  lines.push(
    `**Overall Risk:** ${overallRisk} — ${riskLbl}  ` +
    `**Files:** ${fileReviews.size}  ` +
    `**Issues:** ${totalIssues.critical} critical · ${totalIssues.warning} warnings · ${totalIssues.info} info`
  )
  if (seconds !== '—' || tokStr) {
    const meta = [seconds !== '—' ? `${seconds}s` : null, tokStr].filter(Boolean).join(' · ')
    lines.push(`**Duration:** ${meta}`)
  }
  lines.push('')
  lines.push('---')
  lines.push('')

  // ── Issues by file ───────────────────────────────────────────────────────
  lines.push('## Issues by File')
  lines.push('')

  for (const fr of sortedFileReviews(fileReviews)) {
    const meta     = fileMeta.get(fr.filename)
    const { label: fileLbl } = riskLabel(fr.riskScore)
    const issueWord = fr.mergedIssues.length === 1 ? 'issue' : 'issues'

    lines.push(
      `### \`${fr.filename}\` — Risk ${fr.riskScore} ${fileLbl} · ` +
      `${fr.mergedIssues.length} ${issueWord}`
    )
    lines.push('')

    if (meta) {
      lines.push(`*+${meta.additions} / −${meta.deletions} lines*`)
      lines.push('')
    }

    // Per-chunk summaries (one paragraph per chunk that has a summary)
    const summaries = (fr.chunks ?? []).map((c) => c.summary).filter(Boolean)
    if (summaries.length > 0) {
      summaries.forEach((s) => lines.push(s))
      lines.push('')
    }

    if (fr.mergedIssues.length === 0) {
      lines.push('_No issues found._')
      lines.push('')
      lines.push('---')
      lines.push('')
      continue
    }

    for (const issue of sortedIssues(fr.mergedIssues)) {
      const icon = SEVERITY_ICON[issue.severity] ?? '·'
      lines.push(
        `#### ${icon} ${issue.severity.toUpperCase()} — ${issue.category} (line ${issue.line})`
      )
      lines.push('')
      lines.push(issue.message)
      if (issue.suggestion) {
        lines.push('')
        lines.push(`> **Suggestion:** ${issue.suggestion}`)
      }
      lines.push('')
    }

    lines.push('---')
    lines.push('')
  }

  // ── Security findings table ──────────────────────────────────────────────
  const securityIssues = [...fileReviews.entries()].flatMap(([filename, fr]) =>
    fr.mergedIssues
      .filter((i) => i.category === 'security')
      .map((i) => ({ ...i, _filename: filename }))
  )

  if (securityIssues.length > 0) {
    lines.push('## Security Findings')
    lines.push('')
    lines.push('| Severity | File | Line | Message |')
    lines.push('|---|---|---|---|')
    for (const i of sortedIssues(securityIssues)) {
      // Escape pipe characters inside table cells
      const msg = i.message.replace(/\|/g, '\\|')
      lines.push(`| ${i.severity.toUpperCase()} | \`${i._filename}\` | ${i.line} | ${msg} |`)
    }
    lines.push('')
    lines.push('---')
    lines.push('')
  }

  // ── Suggested commit message ─────────────────────────────────────────────
  if (commitMessage) {
    lines.push('## Suggested Commit Message')
    lines.push('')
    lines.push('```')
    lines.push(commitMessage)
    lines.push('```')
    lines.push('')
    lines.push('---')
    lines.push('')
  }

  // ── Suggested tests ──────────────────────────────────────────────────────
  if (suggestedTests?.length > 0) {
    lines.push('## Suggested Tests')
    lines.push('')
    suggestedTests.forEach((t, i) => lines.push(`${i + 1}. ${t}`))
    lines.push('')
  }

  return lines.join('\n')
}

// ---------------------------------------------------------------------------
// toCSV
// ---------------------------------------------------------------------------

/**
 * Flat CSV — one row per issue. Suitable for spreadsheet import or CI parsing.
 *
 * @param {Map<string,object>} fileReviews
 * @returns {string}
 */
export function toCSV(fileReviews) {
  const rows = [
    ['filename', 'line', 'severity', 'category', 'message', 'suggestion', 'foundBy']
      .map(csvField)
      .join(','),
  ]

  for (const fr of sortedFileReviews(fileReviews)) {
    for (const issue of sortedIssues(fr.mergedIssues)) {
      rows.push(
        [
          fr.filename,
          issue.line,
          issue.severity,
          issue.category,
          issue.message,
          issue.suggestion ?? '',
          issue.foundBy ?? '',
        ]
          .map(csvField)
          .join(',')
      )
    }
  }

  return rows.join('\n')
}

