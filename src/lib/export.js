import { riskLabel } from './scoring.js'

const SEVERITY_RANK = { critical: 3, warning: 2, info: 1 }
const SEVERITY_ICON = { critical: '🔴', warning: '⚠', info: 'ℹ' }

// ---------------------------------------------------------------------------
// Download helper
// ---------------------------------------------------------------------------

export function triggerDownload(filename, content, mimeType) {
  const blob = new Blob([content], { type: mimeType })
  const url  = URL.createObjectURL(blob)
  const a    = document.createElement('a')
  a.href     = url
  a.download = filename
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

/** FileReview[] sorted by riskScore descending */
function sortedFileReviews(fileReviews) {
  return [...fileReviews.values()].sort((a, b) => b.riskScore - a.riskScore)
}

/** Issues sorted: critical → warning → info, then by line number */
function sortedIssues(issues) {
  return [...issues].sort((a, b) => {
    const rankDiff = (SEVERITY_RANK[b.severity] ?? 0) - (SEVERITY_RANK[a.severity] ?? 0)
    return rankDiff !== 0 ? rankDiff : a.line - b.line
  })
}

/** Wrap a CSV field in double-quotes if it contains commas, quotes, or newlines */
function csvField(value) {
  const str = value == null ? '' : String(value)
  if (str.includes(',') || str.includes('"') || str.includes('\n')) {
    return `"${str.replace(/"/g, '""')}"`
  }
  return str
}

